/**
 * All possible orientations for the YASGUI component.
 */
export enum YasguiOrientation {
  /**
   * The vertical orientation, where the query editor is displayed above the result viewer.
   */
  VERTICAL = 'orientation-vertical',
  /**
   * The horizontal orientation, where the query editor is displayed to the left of the result viewer.
   */
  HORIZONTAL = 'orientation-horizontal'
}
